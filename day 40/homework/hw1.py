#შექმენით 5 ელემენტიანი სიტყვების სია. მომხმარებელს შემოატანინეთ სიტყვა და რიცხვი(ეს იქნება პოზიცია სადაც დაამატებთ შემოტანილ სიტყვას). დაბეჭდეთ განახლებული სია.




words = ["მზე", "ცა", "ვარსკვლავი", "მთვარე", "ქარი"]

new_word = input("შეიყვანეთ სიტყვა: ")


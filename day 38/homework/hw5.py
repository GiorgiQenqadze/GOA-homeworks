#შემქნით ცარიელი სია, სადაც 3-ჯერ input-ის სახით მომხმარებელს შეაყვანინებთ სამი სტუდენტის სახელს და დაამატებთ სიაში append() ფუნქციით. შემდეგ კი ჩასვით "Teacher" სიის თავში, წაშალეთ ბოლო სტუდენტი და დაბეჭდეთ სიის სიგრძე, ასვეე საბოლოო სია.




people = []

for i in range(3):
    name = input(f"{i+1}-ე სტუდენტის სახელი: ")
    people.append(name)

people.insert(0, "Teacher")
people.pop()

print("სიის სიგრძეა:", len(people))
print("საბოლოო სიაა:", people)
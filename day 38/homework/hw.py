## .append() ფუნქცია სიის ბოლოს ამატებს ახალ ელემენტს
my_list = [1, 2, 3]
my_list.append(4)  # ახლა სია გახდება [1, 2, 3, 4]

# .insert() ფუნქცია სიის კონკრეტულ ინდექსზე ამატებს ახალ ელემენტს
my_list.insert(1, 99)  # ახლა სია გახდება [1, 99, 2, 3, 4]

# .pop() ფუნქცია სიის ელემენტს აშორებს და აბრუნებს მას
last_item = my_list.pop()  # წაშლის ბოლო ელემენტს (4), სია იქნება [1, 99, 2, 3]
second_item = my_list.pop(1)  # წაშლის ინდექსზე 1-ზე მდგომ ელემენტს (99)
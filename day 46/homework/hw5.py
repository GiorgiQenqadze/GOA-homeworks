#შექმენით ფუნქცია, რომელიც იღებს სიტყვას და აბრუნებს, არის თუ არა ეს სიტყვა პალინდრომი. როგორი სიტყვებია პალინდრომი, შეგიძლიათ გაეცნოთ ქვემოთ დართულ წყაროში:






def aris_palindromi(sityva):
    return sityva == sityva[::-1]

sityva = input("შეიყვანე სიტყვა: ")

if aris_palindromi(sityva):
    print("ეს სიტყვა პალინდრომია.")
else:
    print("ეს სიტყვა არ არის პალინდრომი.")
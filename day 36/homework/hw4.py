#მომხმარებელს შემოატანინეთ წინადადება და სიმბოლო. თქვენი დავალებაა დაითვალოთ რამდენჯერ გვხვდება ეს სიმბოლო წინადადებაში.




sentence = input("შეიყვანეთ წინადადება: ")
char = input("შეიყვანეთ სიმბოლო, რომლის რაოდენობაც გინდათ ითვალოთ: ")

count = sentence.count(char)

print(f"სიმბოლო '{char}' გვხვდება წინადადებაში {count} ჯერ.")